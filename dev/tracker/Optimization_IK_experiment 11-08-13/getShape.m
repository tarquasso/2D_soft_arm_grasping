function [ Shape, InitalAngle ] = getShape(ShapeNumber, ShapeTime, Offset_x, Offset_y)

    if( ShapeNumber == 1)
        Shape = [1.00,  0.00, 1.75;...
                 2.00,  1.5, 1.75;...
                 3.00,  3.62, 2.629;...
                 4.00,  4.5, 4.75;...
                 5.00,  4.5, 7.25;...
                 6.00,  4.5, 9.75];
        Shape = [Shape(:,1).*(ShapeTime/size(Shape(:,1),1)), Shape(:,2).*0.0254 + Offset_x, Shape(:,3).*0.0254 + Offset_y]
        InitalAngle = 0.0;
        
    elseif(ShapeNumber == 2)
        Shape = [1.00,  0.00, 1.75;...
                 2.00,  1.5, 1.75;...
                 3.00,  3.62, 2.629;...
                 4.00,  4.5, 4.75;...
                 5.00,  3.62, 6.87;...
                 6.00,  1.50, 7.75];
        Shape = [Shape(:,1).*(ShapeTime/size(Shape(:,1),1)), Shape(:,2).*0.0254 + Offset_x, Shape(:,3).*0.0254 + Offset_y]
        InitalAngle = 0.0;
        
    elseif(ShapeNumber == 3)
        Shape = [1.00,  0.00, 1.75;...
                 2.00,  1.5, 1.75;...
                 3.00,  3.62, 0.871;...
                 4.00,  4.5, -1.25;...
                 5.00,  5.378, -3.371;...
                 6.00,  7.50, -4.25];
        Shape = [Shape(:,1).*(ShapeTime/size(Shape(:,1),1)), Shape(:,2).*0.0254 + Offset_x, Shape(:,3).*0.0254 + Offset_y]
        InitalAngle = 0.0;
        
    elseif(ShapeNumber == 4)
        Shape = [1.00,  0.00, 1.75;...
                 2.00,  1.5, 1.75;...
                 3.00,  3.62, 0.871;...
                 4.00,  4.5, -1.25;...
                 5.00,  4.5, -3.75;...
                 6.00,  4.5, -6.25];
        Shape = [Shape(:,1).*(ShapeTime/size(Shape(:,1),1)), Shape(:,2).*0.0254 + Offset_x, Shape(:,3).*0.0254 + Offset_y]
        InitalAngle = 0.0;
        
    elseif(ShapeNumber == 5)
        center_x = -0.05;
        center_y = 0.075;
        r = 0.020;
        nsegments = 10;
        ti = pi/2;
        tf = 2*pi + pi/8;
        th = ti:(tf-ti)/nsegments:tf;
        xunit = r * cos(th) + center_x ;
        yunit = r * sin(th) + center_y ;
        
        Shape = [(0:ShapeTime/nsegments:ShapeTime)', xunit', yunit']
        InitalAngle = pi;
        
    else
        Shape = [];
    end

end


